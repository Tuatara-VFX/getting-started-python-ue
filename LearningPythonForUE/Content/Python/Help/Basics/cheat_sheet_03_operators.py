# Arithmetic operators: +, -, *, /, % (modulo), ** (exponent)
result = 10 + 5
result = 10 - 5
result = 10 * 5
result = 10 / 5
result = 10 % 3
result = 2 ** 3

# Comparison operators: ==, !=, >, <, >=, <=
result = 10 == 5
result = 10 != 5
result = 10 > 5
result = 10 < 5
result = 10 >= 5
result = 10 <= 5

# Logical operators: and, or, not
result = (10 > 5) and (20 > 15)
result = (10 > 5) or (20 < 15)
result = not (10 > 5)